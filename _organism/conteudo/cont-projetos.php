<H4 class="sub-titulo">Oque é CMMI</H4>
<p class="texto">
  O CMMI (Capability Maturity Model Integration) foi criado pelo SEI (Software Engineering Institute) é voltado para a capacidade de maturidade de processos de software.
</p>

<p class="texto">
  Um processo representa, dentro da área de software, um conjunto de atividades cujo objetivo é atingir uma meta previamente estipulada. Já por capacidade e maturidade de um processo, deve-se ter a noção do grau de qualidade com o qual um processo atinge um resultado esperado.
</p>

<p class="texto">
  O CMMI está dividido em 5 níveis de maturidade que atestam, por sua vez, o grau de evolução em que uma organização se encontra num determinado momento.
</p>

<ul class="texto" style="list-style-type: none">
  <li>Nível 1 - Inicial: os processos normalmente estão envoltos num caos decorrente da não-obediência ou ainda, inexistência de padrões;</li>
  <li>Nível 2 - Gerenciado: os projetos têm seus requisitos gerenciados neste ponto. Além disso, há o planejamento, a medição e o controle dos diferentes processos;</li>
  <li>Nível 3 - Definido: os processos já estão claramente definidos e são compreendidos dentro da organização. Os procedimentos se encontram padronizados, além de ser preciso prever sua aplicação em diferentes projetos;</li>
  <li>Nível 4 - Gerenciado Quantitativamente: ocorre o aumento da previsibilidade do desempenho de diferentes processos, uma vez que os mesmos já são controlados quantitativamente;</li>
  <li>Nível 5 - Otimizado: existe uma melhoria contínua dos processos.</li>
</ul>

<p class="texto">
  A implantação do CMMI é recomendável para grandes fábricas de software. Implementar os diversos estágios é uma tarefa árdua, não só numa fase inicial, mas também quando se leva em conta a migração de um nível para outro. Isto exigirá, invariavelmente, a realização de vultosos investimentos financeiros, assim como uma mudança de postura da organização (principalmente quando a mesma não contava uma experiência anterior bem-sucedida no gerenciamento de processos). Em inúmeras ocasiões, empresas desenvolvedoras de sistemas recorrem a consultorias especializadas, visando apoio na obtenção da certificação CMMI (fato este que inviabiliza a adoção deste mesmo modelo por pequenas companhias).
</p>


